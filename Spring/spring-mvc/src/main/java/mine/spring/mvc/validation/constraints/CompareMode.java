package mine.spring.mvc.validation.constraints;

/** List of Comparison Modes. */
public enum CompareMode {
    /** Equal Case-Sensitive */
    EQUAL,
    /** Equal Case-Insensitive */
    EQUAL_IGNORE_CASE,
    /** UnEqual Case-Sensitive */
    NOT_EQUAL,
    /** UnEqual Case-Insensitive */
    NOT_EQUAL_IGNORE_CASE
}
