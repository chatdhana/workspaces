package B_Character;

/**
 * The + operator performs string concatenation if and only if at least one of its operands is of type String; otherwise, it
 */
public class LastLaugh {
	public static void main(String args[]) {
		System.out.print("H" + "a");
		System.out.print('H' + "a");
		System.out.print('H' + 'a');
	}
}
